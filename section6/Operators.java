public class Operators {
    public static void main(String[] args)
    {
        int num1 = 123;
        int num2 = 2;
        System.out.println(num1+num2);
        System.out.println(num1/num2);
        System.out.println(num1*num2);
        System.out.println(num1 % num2);
        System.out.println(num1 - num2);
    }
}
